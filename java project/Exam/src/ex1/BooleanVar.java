package ex1;

public class BooleanVar {

	public static void main(String[] args) {
		/*boolean은 참과 거짓을 표현
		 * true와 false 그 자체로 저장이 가능 
		 */
		
		
		boolean b1 = true;
		boolean b2 = false;
		
		int num = 100;
		
		System.out.println(b1);
		System.out.println(b2);
		System.out.println("----------------------------------");
		System.out.println(3<4);
		System.out.println(3>4);
		System.out.println("----------------------------------");
		System.out.println(num > 20);
	}

}
